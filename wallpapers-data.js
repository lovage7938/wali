// Helper functions
function getAllCategories() {
    return Object.keys(wallpapersData);
}

function getWallpapers(category, resolution = '1920x1080') {
    if (wallpapersData[category] && wallpapersData[category][resolution]) {
        return wallpapersData[category][resolution];
    }
    return [];
}

function getCategoryCount(category) {
    if (!wallpapersData[category]) return 0;
    const resolutions = wallpapersData[category];
    const firstRes = Object.keys(resolutions)[0];
    return resolutions[firstRes] ? resolutions[firstRes].length : 0;
}

